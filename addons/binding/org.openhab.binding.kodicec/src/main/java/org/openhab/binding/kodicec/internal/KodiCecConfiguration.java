package org.openhab.binding.kodicec.internal;

/**
 * The {@link KodiCecConfiguration} class contains fields mapping thing configuration parameters.
 *
 * @author Csaba Nagy - Initial contribution
 */
public class KodiCecConfiguration {
    private String ipAddress;
    private Integer port;
    private Integer refreshInterval;

    public String getIpAddress() {
        return ipAddress;
    }

    public void setIpAddress(String ipAddress) {
        this.ipAddress = ipAddress;
    }

    public Integer getPort() {
        return port;
    }

    public void setPort(Integer port) {
        this.port = port;
    }

    public Integer getRefreshInterval() {
        return refreshInterval;
    }

    public void setRefreshInterval(Integer refreshInterval) {
        this.refreshInterval = refreshInterval;
    }

}
